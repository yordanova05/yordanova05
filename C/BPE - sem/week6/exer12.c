#include <stdio.h>

int main() {
    int n;
    printf("Въведете цяло число n: ");
    scanf("%d", &n);

    int height = 2 * (n - 2) + 1;  // Височината на пеперудата

    // Горна част
    for (int i = 0; i < n - 2; i++) {
        // Лява половина
        for (int j = 0; j < n - 1; j++) {
            printf("%c", (j <= i) ? '*' : ' ');
        }

        // Средна част (разстояние и наклонени черти)
        printf("\\ /");

        // Дясна половина
        for (int j = 0; j < n - 1; j++) {
            printf("%c", (j <= i) ? '*' : ' ');
        }

        printf("\n");
    }

    // Среден ред (тяло на пеперудата)
    for (int i = 0; i < n - 1; i++) {
        printf(" ");
    }
    printf("@\n");

    // Долна част (огледално на горната)
    for (int i = n - 3; i >= 0; i--) {
        // Лява половина
        for (int j = 0; j < n - 1; j++) {
            printf("%c", (j <= i) ? '*' : ' ');
        }

        // Средна част (разстояние и наклонени черти)
        printf("/ \\");

        // Дясна половина
        for (int j = 0; j < n - 1; j++) {
            printf("%c", (j <= i) ? '*' : ' ');
        }

        printf("\n");
    }

    return 0;
}
